//switch cases
var a=60000
switch(a){
    case 10000:console.log("user salary is "+a)
    break
    case 20000:console.log("user salary is  "+a)
    break

    case 30000:console.log("user salary is "+a)
    break
    case 40000:console.log("user salary is "+a)
    break
    case 50000:console.log("user salary is "+a)
    break

    case 70000:console.log("user salary is "+a)
    break

    default:console.log("user salary is out of range")
}